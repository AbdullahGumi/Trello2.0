const mongoose = require("mongoose");

const CardSchema = new mongoose.Schema(
  {
    listName: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Card", CardSchema);
